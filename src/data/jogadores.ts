export function fotoLocal(jogador: Jogador): string {
  const slug = jogador.nome
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '');
  return `/players/${jogador.id}-${slug}`;
}

